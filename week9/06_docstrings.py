"""
how to make a multiline comments for python
"""


def func(word):
    """
    a function that returns any word with ', so much' at the end

    :param word: string input of word variable
    :type word: string
    :raise TypeError: If word is not a string
    :return: a string with word with ', so much'
    :rtype: str
    """

    return f"{word}, so much"
